package discgo

import (
	"time"

	"github.com/bwmarrin/snowflake"
)

// Channel represents a channel in Discord.
// If Recipient is set this is a DM Channel otherwise this is a Guild Channel.
// Guild Channel represents an isolated set of users and messages within a Guild.
// DM Channel represent a one-to-one conversation between two users, outside of the
// scope of guilds.
type Channel struct {
	ID                   snowflake.ID `json:"id"`
	GuildID              snowflake.ID `json:"guild_id"`
	Name                 string       `json:"name"`
	Type                 string       `json:"type"`
	Position             int          `json:"position"`
	IsPrivate            bool         `json:"is_private"`
	PermissionOverwrites []*Overwrite `json:"permission_overwrites"`
	Topic                string       `json:"topic"`
	Recipient            *User        `json:"recipient"`
	LastMessageID        snowflake.ID `json:"last_message_id"`
	Bitrate              int          `json:"bitrate"`
	UserLimit            int          `json:"user_limit"`
}

// Message represents a message sent in a channel within Discord.
// The author object follows the structure of the user object, but
// is only a valid user in the case where the message is generated
// by a user or bot user. If the message is generated by a webhook,
// the author object corresponds to the webhook's id, username, and avatar.
// You can tell if a message is generated by a webhook by checking for the
// webhook_id on the message object.
type Message struct {
	ID              snowflake.ID
	ChannelID       snowflake.ID
	Author          *User
	Content         string
	Timestamp       *time.Time
	EditedTimestamp *time.Time // nullable
	TTS             bool
	MentionEveryone bool
	Mentions        []*User
	MentionRoles    []snowflake.ID
	Attachments     []*Attachment
	Embeds          []*Embed
	Reactions       []*Reaction
	Nonce           snowflake.ID
	Pinned          bool
	WebhookID       string
}

type Reaction struct {
	Count int
	Me    bool
	Emoji *ReactionEmoji
}

type ReactionEmoji struct {
	ID   *snowflake.ID // nullable
	Name string
}

type Overwrite struct {
	ID    snowflake.ID
	Type  string
	Allow int
	Deny  int
}

type Embed struct {
	Title       string
	Type        string
	Description string
	URL         string
	Timestamp   time.Time
	Color       int
	Footer      *EmbedFooter
	Image       *EmbedImage
	Thumbnail   *EmbedThumbnail
	Video       *EmbedVideo
	Provider    *EmbedProvider
	Author      *EmbedAuthor
	Fields      []*EmbedField
}

type EmbedThumbnail struct {
	URL      string
	ProxyURL string
	Height   int
	Width    int
}

type EmbedVideo struct {
	URL    string
	Height int
	Width  int
}

type EmbedImage struct {
	URL      string
	ProxyURL string
	Height   int
	Width    int
}

type EmbedProvider struct {
	Name string
	URL  string
}

type EmbedAuthor struct {
	Name         string
	URL          string
	IconURL      string
	ProxyIconURL string
}

type EmbedFooter struct {
	Text         string
	IconURL      string
	ProxyIconURL string
}

type EmbedField struct {
	Name   string
	Value  string
	Inline bool
}

type Attachment struct {
	ID       snowflake.ID
	Filename string
	Size     int
	URL      string
	ProxyURL string
	Height   int
	Width    int
}

